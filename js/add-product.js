// Load product data into HTML
function loadProductData() {
    // Generate unique ID if not set
    if (!product.id || product.id === 1) {
        const products = JSON.parse(localStorage.getItem('products')) || [];
        product.id = products.length > 0 ? Math.max(...products.map(p => p.id)) + 1 : 1;
    }
    // Update breadcrumb
    const breadcrumbContainer = document.querySelector('.breadcrumb');
    if (breadcrumbContainer) {
        breadcrumbContainer.innerHTML = product.breadcrumb.map((item, index) => {
            if (index === product.breadcrumb.length - 1) {
                return `<li class="breadcrumb-item active">${item.name}</li>`;
            }
            return `<li class="breadcrumb-item"><a href="${item.url}">${item.name}</a></li>`;
        }).join('');
    }
    
    // Update product badge
    const badgeElement = document.querySelector('.new-badge');
    if (badgeElement) {
        badgeElement.textContent = product.badge;
    }
    
    // Update product title
    const titleElement = document.querySelector('.product-title');
    if (titleElement) {
        titleElement.textContent = product.name;
    }
    
    // Update product code
    const codeElement = document.querySelector('.product-code strong');
    if (codeElement) {
        codeElement.textContent = product.code;
    }
    
    // Update price
    const priceElement = document.querySelector('.price-amount');
    if (priceElement) {
        priceElement.textContent = `${product.price.toFixed(2)} ${product.currency}`;
    }
    
    const unitElement = document.querySelector('.price-unit');
    if (unitElement) {
        unitElement.textContent = product.unit;
    }
    
    // Update net price
    const netPriceElement = document.querySelector('.net-price');
    if (netPriceElement) {
        netPriceElement.textContent = `net price ${product.netPrice.toFixed(2)} ${product.currency}`;
    }
    
    // Update production time
    const productionTimeElement = document.querySelector('.production-time span');
    if (productionTimeElement) {
        productionTimeElement.innerHTML = `Estimated production time: <strong>${product.productionTime}</strong>`;
    }
    
    // Update gallery images
    const mainImage = document.getElementById('mainProductImage');
    if (mainImage) {
        mainImage.src = images[0];
        mainImage.alt = product.name;
    }
    
    const thumbnails = document.querySelectorAll('.thumbnail');
    thumbnails.forEach((thumb, index) => {
        if (images[index]) {
            thumb.src = images[index];
            thumb.alt = product.name;
        }
    });
    
    // Update additional products
    const additionalContainer = document.querySelector('.additional-products');
    if (additionalContainer && product.additionalProducts) {
        additionalContainer.innerHTML = product.additionalProducts.map(addon => {
            if (addon.image) {
                return `
                    <div class="additional-item">
                        <img src="${addon.image}" alt="${addon.name}" class="addon-image">
                        <div class="addon-info">
                            <span class="addon-name">${addon.name}</span>
                            <span class="addon-price">+${addon.price.toFixed(2)} ${product.currency}</span>
                        </div>
                    </div>
                `;
            } else {
                return `
                    <div class="additional-item">
                        <div class="stripe-selector">
                            <span class="stripe-label">${addon.name} - ${addon.defaultOption}</span>
                            <select class="stripe-select">
                                ${addon.options.map(option => 
                                    `<option ${option === addon.defaultOption ? 'selected' : ''}>${option}</option>`
                                ).join('')}
                            </select>
                            <span class="stripe-price">+${addon.price.toFixed(2)} ${product.currency}</span>
                        </div>
                    </div>
                `;
            }
        }).join('');
    }
    
    // Update product description
    const descriptionContainer = document.querySelector('.product-details');
    if (descriptionContainer && product.description) {
        descriptionContainer.innerHTML = `
            <h3>${product.description.title}</h3>
            <div class="description-content">
                <p>${product.description.content}</p>
                
                <h4>Features:</h4>
                <ul>
                    ${product.description.features.map(feature => `<li>${feature}</li>`).join('')}
                </ul>
                
                <h4>Specifications:</h4>
                <div class="specifications">
                    ${product.description.specifications.map(spec => 
                        `<div class="spec-item"><strong>${spec.label}:</strong> ${spec.value}</div>`
                    ).join('')}
                </div>
            </div>
        `;
    }
}

// Change main product image
function changeMainImage(thumbnail, index) {
    const mainImage = document.getElementById('mainProductImage');
    mainImage.src = thumbnail.src;
    currentImageIndex = index;
    
    // Update active thumbnail
    document.querySelectorAll('.thumbnail').forEach(thumb => {
        thumb.classList.remove('active');
    });
    thumbnail.classList.add('active');
}

// Navigate to previous image
function previousImage() {
    currentImageIndex = currentImageIndex > 0 ? currentImageIndex - 1 : images.length - 1;
    updateMainImage();
}

// Navigate to next image
function nextImage() {
    currentImageIndex = currentImageIndex < images.length - 1 ? currentImageIndex + 1 : 0;
    updateMainImage();
}

// Update main image and active thumbnail
function updateMainImage() {
    const mainImage = document.getElementById('mainProductImage');
    const thumbnails = document.querySelectorAll('.thumbnail');
    
    mainImage.src = images[currentImageIndex];
    
    thumbnails.forEach((thumb, index) => {
        thumb.classList.toggle('active', index === currentImageIndex);
    });
}

// Scroll thumbnails
function scrollThumbnails(direction) {
    const container = document.querySelector('.thumbnail-container');
    const scrollAmount = 100;
    
    if (direction === 'left') {
        container.scrollLeft -= scrollAmount;
    } else {
        container.scrollLeft += scrollAmount;
    }
}

